export const regulations: Regulation[] = regulationSeeds.map((seed) => ({
  ...seed,
  language:
    seed.country === '中国'
      ? 'zh-CN'
      : seed.country === '日本'
        ? 'ja-JP'
        : 'en-US',
  translatedLanguage: seed.country === '中国' ? 'en-US' : 'zh-CN',
  articles: makeArticles(seed.id, seed.title),
  relatedIds: regulationSeeds
    .filter((item) => item.country === seed.country && item.id !== seed.id)
    .slice(0, 2)
    .map((item) => item.id),
}));

export const latestRegulations = regulations
  .slice()
  .sort((left, right) => right.publishDate.localeCompare(left.publishDate))
  .slice(0, 6);

export const regulationCountries = Array.from(
  new Set(regulations.map((item) => item.country)),
);

export const regulationIndustries = Array.from(
  new Set(regulations.flatMap((item) => item.industries)),
).sort((left, right) => left.localeCompare(right, 'zh-CN'));

export const regulationLevels = Array.from(
  new Set(regulations.map((item) => item.level)),
);

export const regulationTopics = Array.from(
  new Set(regulations.flatMap((item) => item.topics)),
).sort((left, right) => left.localeCompare(right, 'zh-CN'));

export const findRegulationById = (id?: string) =>
  regulations.find((item) => item.id === id);
